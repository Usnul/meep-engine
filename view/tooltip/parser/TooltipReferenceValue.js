export class TooltipReferenceValue {
    /**
     *
     * @param {string} type
     * @param {Object<String>} values
     */
    constructor(type, values) {
        /**
         *
         * @type {string}
         */
        this.type = type;
        /**
         *
         * @type {Object<String>}
         */
        this.values = values;
    }
}
